package com.javarush.task.task04.task0437;


/* 
Треугольник из восьмерок
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        for (int y = 1; y <= 10; y++) {
            for (int i = 1; i < y; i++) {
                System.out.print("8");
            }
            System.out.println(8);
        }
    }
}